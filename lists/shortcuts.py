# Shortcuts to know:
# ctrl + z - one action back
# ctrl + x - cutting entire row
# ctrl + c - paste the row

# ctrl + d - duplicates the line, copies the current line and pass to the next line
# ctrl + y - deletes the line (first time pycharm asks to select the option)
# ctrl + / - to comment and uncomment highlighted lines
# ctrl + s - save the file
# ctrl + shift + up/down arrow - moves the line up and down
# ctrl + alt + L - PEP formats the highlighted text/code string